export const svgFilled = (path: string): string =>
  `<svg aria-hidden="true" fill="currentColor" height="16" viewBox="0 0 16 16" width="16">${path}</svg>`

export const svgOutline = (path: string): string =>
  `<svg aria-hidden="true" fill="none" height="16" stroke="currentColor" stroke-linecap="round" stroke-linejoin="round" stroke-width="1.5" viewBox="0 0 24 24" width="16">${path}</svg>`
